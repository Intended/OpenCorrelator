    private void jData_ListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jData_ListActionPerformed
        // TODO add your handling code here:
        String check = (String) jData_List.getModel().getSelectedItem();
        if (check !=null &&  !check.isEmpty()){
            jLabel_pearson.setVisible(true);
            jLabel_spearman.setVisible(true);
            jLabel_info.setVisible(true);
            jLabel_help.setVisible(false);
            for(int i = 0; i < results.length; i++){
                if(check.equals(results[i].dataset1 + " " + results[i].dataset2)){
                    jLabel2.setText(Float.toString(results[i].pearson));
                    jLabel3.setText(Float.toString(results[i].spearman));
                    // String 
                    jLabel_info.setText(results[i].corr);
                }
            }
        }
        else
        {
            jLabel_pearson.setVisible(false);
            jLabel_spearman.setVisible(false);
            jLabel_info.setVisible(false);
            jLabel_help.setVisible(true);
        }
    }//GEN-LAST:event_jData_ListActionPerformed

