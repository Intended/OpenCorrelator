    private void storeList(Results[] result){
        String[] arrStr = new String[result.length];
        DefaultComboBoxModel dlm = new DefaultComboBoxModel();
        dlm.addElement("");
        for(int i = 0; i < result.length; i++){
            arrStr[i] = result[i].dataset1 + " " + result[i].dataset2;
            dlm.addElement(arrStr[i]);
        }
        jData_List.setModel(dlm);
    }
